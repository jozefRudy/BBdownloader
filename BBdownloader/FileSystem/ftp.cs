            ftpRequest.UsePassive = true;
            ftpRequest.KeepAlive = true;

            try
            {
                using ((FtpWebResponse)ftpRequest.GetResponse())
                {
                    directoryExists = true;
                }
            }
            catch (WebException)
            {
                directoryExists = false;
            }


